function drawChessboard(size) {
  // empty string
  let grid = "";
  // for loop that takes the parameter as the conditional limit
for(let x = 0; x < size; x++){
  //nested for loop that takes the parameter as the conditional limit
  for(let y = 0; y < size; y++){
    if((y+x) % 2 ==0){grid += " "}
    else {grid += "#"
  }
  }
  grid += "\n"
}
console.log(grid)
}


