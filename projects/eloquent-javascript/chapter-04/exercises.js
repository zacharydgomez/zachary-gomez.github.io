// reverseArray ////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArray(arr) {
  let rev = [];
  // reverse loop
  for(let i = arr.length - 1; i >= 0; i--){
    rev.push(arr[i]);
  }
  return rev;

}

////////////////////////////////////////////////////////////////////////////////
// reverseArrayInPlace /////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArrayInPlace(arr) {
  
  for(let i = arr.length -1; i >= 0; i--){
    //pushing the reversed array into the array
    arr.push(arr[i])
    
  }
  //splicing off the first half of the array
  arr.splice(0, (arr.length) /2)
//returning the array
return arr
}

