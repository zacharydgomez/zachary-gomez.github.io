////////////////////////////////////////////////////////////////////////////////

function sum(arr) {
  // empty variable for output
  let output = 0
  // loop through array
  for(let i = 0; i < arr.length; i++){
    // adding indexes together in output
output += arr[i]
  }
  //returning the output
return output
}

