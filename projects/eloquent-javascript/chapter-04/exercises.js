// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end, step) {
  //empty arr
  let arr = []
// if step is positive
if( step > 0){
  while( start <= end){
 arr.push(start)
 start += step
}
}
//if step is negative
if(step < 0){
  while(start >= end){
    arr.push(start)
    start += step
  }
  
}
//if there is no step and the start and end are the same
if(!step && start === end){
  return arr
}
// if there is no step
if( !step){
  while (start <= end){
    arr.push(start)
    start++
  }
  
}
return arr
}

