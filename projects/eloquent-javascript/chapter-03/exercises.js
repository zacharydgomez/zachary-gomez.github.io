function countChars(str, ltr) {
  //empty variable for coutning my ltrs
let count = 0
//loop to iterate through the string
for(let i = 0; i< str.length; i++){
  // adds one for each letter found
  if(str[i] === ltr){count ++}
}
//returns the count
return count
}

